	public function history() {
        if (!$this->customer->isLogged()) {
            $this->response->setOutput('');
            return;
        }
        if (!$this->customer->isSeller()) {
            $this->response->setOutput('');
            return;
        }

        $this->load->language('seller/order');

		$data['text_no_results'] = $this->language->get('text_no_results');

		$data['column_date_added'] = $this->language->get('column_date_added');
		$data['column_status'] = $this->language->get('column_status');
		$data['column_notify'] = $this->language->get('column_notify');
		$data['column_comment'] = $this->language->get('column_comment');

		if (isset($this->request->get['page'])) {
			$page = $this->request->get['page'];
		} else {
			$page = 1;
		}

		$data['histories'] = array();

		$this->load->model('seller/order');

		$results = $this->model_seller_order->getOrderHistories($this->request->get['order_id'], ($page - 1) * 10, 10);

		foreach ($results as $result) {
            $comment = $result['comment'];
            if (preg_match('/^a:\d{1}:\{.*/i', $result['comment'])) {
                $comment = unserialize($result['comment']);
            }
            if (is_array($comment)) {
                $comment = join("\n", $comment);
            } else {
                $comment = $result['comment'];
            }

            $data['histories'][] = array(
				'notify'     => $result['notify'] ? $this->language->get('text_yes') : $this->language->get('text_no'),
				'status'     => $result['status'],
				'comment'    => nl2br($comment),
				'date_added' => date($this->language->get('date_format_short'), strtotime($result['date_added']))
			);
		}

		$history_total = $this->model_seller_order->getTotalOrderHistories($this->request->get['order_id']);

		$pagination = new Pagination();
		$pagination->total = $history_total;
		$pagination->page = $page;
		$pagination->limit = 10;
		$pagination->url = $this->url->link('seller/order/history', 'order_id=' . $this->request->get['order_id'] . '&page={page}', 'SSL');

		$data['pagination'] = $pagination->render();

		$data['results'] = sprintf($this->language->get('text_pagination'), ($history_total) ? (($page - 1) * 10) + 1 : 0, ((($page - 1) * 10) > ($history_total - 10)) ? $history_total : ((($page - 1) * 10) + 10), $history_total, ceil($history_total / 10));

        if (file_exists(DIR_TEMPLATE . $this->config->get('config_template') . '/template/seller/order_history.tpl')) {
            $this->response->setOutput($this->load->view($this->config->get('config_template') . '/template/seller/order_history.tpl', $data));
        } else {
            $this->response->setOutput($this->load->view('default/template/seller/order_history.tpl', $data));
        }
	}

