	public function delivery() {
		$this->load->language('seller/order');

        $this->load->language('api/order');

        $json = array();

        if (!$this->customer->isLogged() || !$this->customer->isSeller()) {
            $json['error'] = $this->language->get('error_permission');
        } elseif (!isset($this->request->post['status']) || !isset($this->request->post['name']) || !isset($this->request->post['number']) || empty($this->request->post['name']) || empty($this->request->post['number'])) {
            $json['error'] = $this->language->get('error_required_check');
        } else {
            $delivery_data = array(
                'name' => trim($this->request->post['name']),
                'number' => trim($this->request->post['number']),
                'comment' => trim($this->request->post['comment'])
            );

            //$delivery_data = serialize($delivery_data);

            $this->load->model('seller/order');

            if (isset($this->request->get['order_id'])) {
                $order_id = $this->request->get['order_id'];
            } else {
                $order_id = 0;
            }

            $order_info = $this->model_seller_order->getOrder($order_id);

            if ($order_info) {
                if (in_array((int)$order_info['order_status_id'], array(2,15))) {
                    if ((int)$order_info['order_status_id'] == 15) {
                        $status = 3;
                    } else {
                        $status = $this->request->post['status'] ? 3 : 15;
                    }
                    $this->load->model('checkout/order');
                    $this->model_checkout_order->addOrderHistory($order_id, $status, $delivery_data, true);

                    $json['success'] = $this->language->get('text_success');
                } else {
                    $json['error'] = $this->language->get('error_order_status');
                }

            } else {
                $json['error'] = $this->language->get('error_not_found');
            }
        }

        $this->response->addHeader('Content-Type: application/json');
        $this->response->setOutput(json_encode($json));
	}
